    public void aff()//Affichage de 10 Dernier consultation
    {
        try {
            state = (Statement) connection.getInstance().createStatement();
            result = state.executeQuery("SELECT consultation.Id_consultation,consultation.Date, patient.Nom,patient.Post_nom,patient.Prenom, patient.Sexe,consultation.s, consultation.A, traitement.Medicament FROM consultation,patient, traitement WHERE consultation.Id_pa=patient.id_pa AND consultation.Id_consultation=traitement.Id_cons AND patient.id_pa=traitement.Id_pa   ORDER BY consultation.Id_consultation DESC LIMIT 0,30");
            DefaultTableModel Table=  (DefaultTableModel) Tb_liste.getModel();
            String no;
            Table.setColumnCount(0);

                   Table.addColumn("ID");
                   Table.addColumn("Date");
                   Table.addColumn("Nom");
                   Table.addColumn("Post_nom");
                   Table.addColumn("Prenom");
                   Table.addColumn("Sexe");
                   Table.addColumn("Symptomes Dominant");
                   Table.addColumn("Diagnostique");
                   Table.addColumn("Traitement");
                   Table.setRowCount(0);
            while (result.next())
                      {//
                          /*no=result.getString("Nom");
                          System.out.println(no);*/
                          Object[] objets= new Object[9];
                            objets[0]= result.getString("Id_consultation");
                            objets[1]= result.getString("Date");
                            objets[2]= result.getString("Nom");
                            objets[3]= result.getString("Post_nom");
                            objets[4]= result.getString("Prenom");
                            objets[5]= result.getString("Sexe");
                            objets[6]= result.getString("s");
                            objets[7]= result.getString("A");
                            objets[8]= result.getString("Medicament");
                            Table.addRow(objets);
                      }
        } catch (SQLException ex) {
            Logger.getLogger(Rapport_zone_de_sante.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
            
